static
struct evtr_variable *
evtr_var_new(const char *name)
{
	struct evtr_variable *var;

	var = calloc(1, sizeof(*var));
	if (var) {
		if (!(var->name = strdup(name))) {
			free(var);
			return NULL;
		}
		var->val.type = EVTR_VAL_NIL;
	}
	return var;
}


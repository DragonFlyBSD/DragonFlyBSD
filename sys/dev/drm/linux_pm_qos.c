#include <linux/pm_qos.h>

#include <sys/param.h>
#include <machine/cpu.h>

void
pm_qos_update_request(struct pm_qos_request *handle, int target_value)
{
	if (target_value == 0) {
		cpu_inhibit_deep_sleep(1);
	} else {
		cpu_inhibit_deep_sleep(0);
	}
}
